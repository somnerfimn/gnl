#include "libft.h"

void	ft_bzero(void *str, size_t n)
{
	unsigned char *tmp;

	tmp = (unsigned char *)str;
	while (n != 0)
	{
		*tmp = '\0';
		n--;
		if (n)
			tmp++;
	}
}
