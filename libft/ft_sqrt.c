#include "libft.h"

int		ft_sqrt(int nb)
{
	int		tmp;

	tmp = 1;
	if (nb <= 0)
		return (0);
	while (tmp != (nb / 2) + 1)
	{
		if (tmp * tmp == nb)
			return (tmp);
		tmp++;
	}
	return (0);
}
